static const uint16_t IMAGE_NT_OPTIONAL_HDR32_MAGIC = 0x10b;
static const uint16_t IMAGE_NT_OPTIONAL_HDR64_MAGIC = 0x20b;

static const size_t IMAGE_DIRECTORY_ENTRY_CERTIFICATE = 4;

#pragma pack(push,1)

